create table tb_user (
	id BIGINT NOT NULL COMMENT 'ID',
	create_time DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
	update_time TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '修改时间',
	version INT NOT NULL DEFAULT 0 COMMENT '版本号',
	deleted VARCHAR(10) NOT NULL DEFAULT 'NO' COMMENT '删除状态',
	nickname VARCHAR(100) NOT NULL DEFAULT '' COMMENT '昵称',
    avatar  VARCHAR(500)  NOT NULL DEFAULT '' COMMENT '头像',
    about_me VARCHAR(500) NOT NULL DEFAULT '' COMMENT '关于我',
    sex  VARCHAR(10)  NOT NULL DEFAULT '' COMMENT '性别',
    nickname_state VARCHAR(20) NOT NULL DEFAULT 'UNCHECKED' COMMENT '昵称审核状态',
    avatar_state VARCHAR(20) NOT NULL DEFAULT 'UNCHECKED' COMMENT '头像审核状态',
    about_me_state VARCHAR(20) NOT NULL DEFAULT 'UNCHECKED' COMMENT '签名审核状态',
    birthday DATE  DEFAULT NULL COMMENT '生日',
    platform VARCHAR(20) NOT NULL DEFAULT 'IOS' COMMENT '注册平台',
    country VARCHAR(50)  COMMENT '国家',
	province VARCHAR(50)  COMMENT '省份/州',
	city VARCHAR(50)  COMMENT '城市',
	lon DECIMAL COMMENT '经度',
    lat DECIMAL COMMENT '纬度',
    show_videos_state VARCHAR(20) NOT NULL DEFAULT 'ALL' COMMENT '显示状态',
    active VARCHAR(20) NOT NULL DEFAULT 'ACTIVE' COMMENT '账号状态',
    verify_video_state VARCHAR(20) NOT NULL DEFAULT 'UNCHECKED' COMMENT '认证视频审核状态',
	PRIMARY KEY (id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='用户表';